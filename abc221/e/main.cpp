// ref. arihon 3-3
// E: type of data, R: type of query result
template<typename E, typename R>
class SegmentTree {
    public:
        SegmentTree(const int _n, const E inf, R (*f)(E, E)): f(f), inf(inf) {
            n = 1;
            while (n < _n) n *= 2;
            data.resize(2 * n, inf);
        }

        // update data[k] with a
        // 0-index, [0, _n) in other words
        // time complexity: O(n)
        void update(int k, E a) {
            k += n - 1;
            data[k] = a;
            while (k > 0) {
                k = (k - 1) / 2;
                data[k] = (*f)(data[k * 2 + 1], data[k * 2 + 2]);
            }
        }

        // query value from [a, b)
        // 0-index, [0, _n) in other words
        // time complexity: O(n)
        R query(int a, int b) {
            return query(a, b, 0, 0, n);
        }

    private:
        int n;
        R (*f)(E, E);
        const E inf;
        vector<E> data;

        R query(int a, int b, int k, int l, int r) {
            // if [a, b) intersect [l, r) = empty
            if (r <= a || b <= l) {
                return inf;
            }

            // if [a, b) includes [l, r)
            if (a <= l && r <= b) {
                return data[k];
            } else {
                R vl = query(a, b, k * 2 + 1, l, (l + r) / 2);
                R vr = query(a, b, k * 2 + 2, (l + r) / 2, r);
                return (*f)(vl, vr);
            }
        }
};

const ll MOD = 998244353;

// auto mod int
// ref. https://github.com/atcoder-live/library/blob/master/mint.cpp
// https://youtu.be/L8grWxBlIZ4?t=9858
// https://youtu.be/ERZuLAxZffQ?t=4807 : optimize
// https://youtu.be/8uowVvQ_-Mo?t=1329 : division
struct mint {
  ll x; // typedef long long ll;
  mint(ll x=0):x((x%MOD+MOD)%MOD){}
  mint operator-() const { return mint(-x);}
  mint& operator+=(const mint a) {
    if ((x += a.x) >= MOD) x -= MOD;
    return *this;
  }
  mint& operator-=(const mint a) {
    if ((x += MOD-a.x) >= MOD) x -= MOD;
    return *this;
  }
  mint& operator*=(const mint a) {
    (x *= a.x) %= MOD;
    return *this;
  }
  mint operator+(const mint a) const {
    mint res(*this);
    return res+=a;
  }
  mint operator-(const mint a) const {
    mint res(*this);
    return res-=a;
  }
  mint operator*(const mint a) const {
    mint res(*this);
    return res*=a;
  }
  mint pow(ll t) const {
    if (!t) return 1;
    mint a = pow(t>>1);
    a *= a;
    if (t&1) a *= *this;
    return a;
  }

  // for prime MOD
  mint inv() const {
    return pow(MOD-2);
  }
  mint& operator/=(const mint a) {
    return (*this) *= a.inv();
  }
  mint operator/(const mint a) const {
    mint res(*this);
    return res/=a;
  }
};

const int MAX_N = 300000;

map<int, int> prepare_converter(const vector<int> &as) {
    set<int> bs;
    for (auto a : as) {
        bs.insert(a);
    }

    map<int, int> ans;
    {
        auto it = bs.begin();
        int idx = 0;
        while (it != bs.end()) {
            ans[*it] = idx;
            it++;
            idx++;
        }
    }

#ifdef DEBUG
    printf("converter: \n");
    for (auto p : ans) {
        printf("  %d: %d\n", p.first, p.second);
    }
#endif

    return ans;
}

int main(void) {
    int N;
    cin >> N;

    vector<int> as(N);
    for (int i = 0; i < N; i++) {
        cin >> as[i];
    }

    map<int, int> converter = prepare_converter(as);

    auto f = [](mint x, mint y) {
        return x + y;
    };
    SegmentTree<mint, mint> *st = new SegmentTree<mint, mint>(MAX_N + 1, 0, f);

    mint ans = 0;
    mint twos = mint(2).pow(N);

    for (int i = N - 1; i >= 0; i--) {
        const int x = converter[as[i]];
        const mint acc = st->query(x, MAX_N + 1) / twos;
#ifdef DEBUG
        printf("%d: %lld\n", as[i], acc.x);
#endif
        ans += acc;
        twos /= 2;
        st->update(x, st->query(x, x + 1) + twos);
    }

    cout << ans.x << endl;

    delete st;
    return 0;
}
